
public class First {

	//접근제한자가 부여된 멤버변수
	private int a; //클래스 안에서만 사용하고 싶을때
	int b;
	protected int c;
	public int d;
	
	
	
	//멤버메소드
	void aaa() {
		//같은 클래스의 private멤버에 접근 당연히 가능
		a=20;
		//다른 제한자들은 당연히 모두 가능
		b=1;
		c=2;
		d=3;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
	}
	
	
}//First class
